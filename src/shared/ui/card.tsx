import { forwardRef, type HTMLAttributes } from "react";
import { cn } from "../lib/utils";

// shadcn 스타일 Card 계열. 색상은 프로젝트 semantic 토큰에 연결.
export const Card = forwardRef<HTMLDivElement, HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(
        "rounded-xl border border-surface-border-soft bg-surface-raised text-text-primary shadow-lg",
        className,
      )}
      {...props}
    />
  ),
);
Card.displayName = "Card";

